package com.example.foodrecommendation.model.logmeal;

public class Preferred{
	private Object quantity;
	private int gramsConversion;
	private String name;
	private String jsonMemberShort;
	private int id;
	private String state;
	private String type;

	public Object getQuantity(){
		return quantity;
	}

	public int getGramsConversion(){
		return gramsConversion;
	}

	public String getName(){
		return name;
	}

	public String getJsonMemberShort(){
		return jsonMemberShort;
	}

	public int getId(){
		return id;
	}

	public String getState(){
		return state;
	}

	public String getType(){
		return type;
	}
}
